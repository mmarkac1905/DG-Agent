"""Hotfix #65 — tests for scope re-derivation guard.

Validates that `_has_deployed_s2t_rows` correctly distinguishes
Stage A stub rows (target_model empty) from Piece 8 Deploy rows
(target_model populated), and that the guard sentinel path in
`confirm_scope` is reachable.

The full `confirm_scope` integration path (propose→revise→confirm)
is exercised by scripts/regression_piece8.py scenario 22 and not
duplicated here.
"""
from __future__ import annotations

import csv
import sys
from pathlib import Path

_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(_ROOT / "scripts"))

import _scope_derivation as sd  # noqa: E402


_S2T_FIELDS = [
    "id", "business_term_id", "business_term_name", "source_table",
    "source_field", "source_description", "target_model", "target_column",
    "transformation_logic_plain", "transformation_logic_sql",
    "join_description", "filter_description", "notes",
    "transformation_logic_plain_business",
]


def _write_csv(path: Path, rows: list[dict]) -> None:
    with path.open("w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=_S2T_FIELDS, lineterminator="\n",
                           quoting=csv.QUOTE_MINIMAL)
        w.writeheader()
        for r in rows:
            w.writerow({k: r.get(k, "") for k in _S2T_FIELDS})


def test_rewrite_s2t_for_term_proceeds_with_stage_a_stub_only(
    tmp_path, monkeypatch,
) -> None:
    """Happy path: only Stage A stub rows (target_model empty) → guard
    returns False, confirm_scope path would proceed."""
    tmp_csv = tmp_path / "s2t_mapping.csv"
    _write_csv(tmp_csv, [
        {"id": "S2T-0001", "business_term_id": "BG999",
         "source_table": "EKKO", "source_field": "EBELN",
         "notes": "stage_a_derived"},
        {"id": "S2T-0002", "business_term_id": "BG999",
         "source_table": "EKPO", "source_field": "MENGE",
         "notes": "stage_a_derived"},
    ])
    monkeypatch.setattr(sd, "_S2T_CSV", tmp_csv)

    has_deployed, count = sd._has_deployed_s2t_rows("BG999")
    assert has_deployed is False
    assert count == 0


def test_rewrite_s2t_for_term_blocks_with_deployed_rows(
    tmp_path, monkeypatch,
) -> None:
    """Blocked path: at least one row with target_model populated →
    guard returns True with count. confirm_scope would return
    ConfirmationResult(success=False, error=<...>).

    CSV file on disk must NOT be modified by the check itself."""
    tmp_csv = tmp_path / "s2t_mapping.csv"
    original_rows = [
        {"id": "S2T-0010", "business_term_id": "BG888",
         "source_table": "EKKO", "source_field": "EBELN",
         "target_model": "fact_purchase_orders",
         "target_column": "purchase_order_number",
         "notes": "Generated by Claude create_s2t_with_implementation"},
        {"id": "S2T-0011", "business_term_id": "BG888",
         "source_table": "EKKO", "source_field": "LIFNR",
         "target_model": "",  # mixed: this one is stub
         "notes": "stage_a_derived"},
    ]
    _write_csv(tmp_csv, original_rows)
    monkeypatch.setattr(sd, "_S2T_CSV", tmp_csv)

    # Snapshot file bytes before guard check
    bytes_before = tmp_csv.read_bytes()

    has_deployed, count = sd._has_deployed_s2t_rows("BG888")
    assert has_deployed is True
    assert count == 1, f"expected 1 deployed row, got {count}"

    # Guard is read-only — CSV must be byte-identical after check
    bytes_after = tmp_csv.read_bytes()
    assert bytes_before == bytes_after, "guard mutated s2t_mapping.csv"


def test_rewrite_s2t_for_term_unknown_term_is_false(
    tmp_path, monkeypatch,
) -> None:
    """Defense-in-depth: term_id not in CSV → no false blocks."""
    tmp_csv = tmp_path / "s2t_mapping.csv"
    _write_csv(tmp_csv, [
        {"id": "S2T-0001", "business_term_id": "BG111",
         "source_table": "EKKO", "source_field": "EBELN",
         "target_model": "fact_purchase_orders"},
    ])
    monkeypatch.setattr(sd, "_S2T_CSV", tmp_csv)

    has_deployed, count = sd._has_deployed_s2t_rows("BG999_does_not_exist")
    assert has_deployed is False
    assert count == 0


